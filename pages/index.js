import React, { useState } from "react";
import { useInView } from "react-intersection-observer";
import Head from "next/head";
import Header from "../components/sections/Header";
import Home from "../components/sections/Home";
import About from "../components/sections/About";
import Projects from "../components/sections/Projects";
import Contact from "../components/sections/Contact";
import CommandLine from "../components/CommandLine";

export default function Index() {
  const [darkMode, setDarkMode] = useState(true);

  const sections = {
    home: {
      title: "Fabio Lima.",
      nav: "HOME",
      color: "gradient-oceanic",
      id: "home",
    },
    about: {
      title: "Sobre.",
      nav: "SOBRE",
      color: "gradient-sunset",
      id: "sobre",
    },
    projects: {
      title: "Projetos.",
      nav: "PROJETOS",
      color: "gradient-hyper",
      id: "projetos",
    },
    contact: {
      title: "Contato.",
      nav: "CONTATO",
      color: "gradient-candy",
      id: "contato",
    },
  };

  Object.keys(sections).forEach((key) => {
    [sections[key].ref, sections[key].inView] = useInView({
      threshold: 0.5,
    });
  });

  return (
    <div
      className={`${
        darkMode ? "dark-theme" : "light-theme"
      } box-border flex flex-col items-center bg-primary px-5 xs:px-10 md:px-20`}
    >
      <Head>
        <title>Fabio Lima</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header
        darkMode={darkMode}
        setDarkMode={setDarkMode}
        sections={sections}
      />
      <main className="w-full max-w-screen-lg">
        <Home section={sections.home} />
        <About section={sections.about} />
        <Projects />
        <Contact />
        <CommandLine />
      </main>
    </div>
  );
}
